export type Translations = typeof translations.it;

// ── Store ─────────────────────────────────────────────────────────────────────

const DEFAULT_LANG: Lang = 'it';
const saved = browser ? (localStorage.getItem('lang') as Lang | null) : null;
const initial: Lang = (saved && saved in translations) ? saved as Lang : DEFAULT_LANG;

export const lang = writable<Lang>(initial);

// Persist to localStorage
lang.subscribe(l => {
  if (browser) localStorage.setItem('lang', l);
});

/** Reactive translations object */
export const t = derived(lang, $lang => translations[$lang] as Translations);

export function setLang(l: Lang) {
  lang.set(l);
}

